package demo;

public class Switchdemo {

	public static void main(String[] args)
	{
		int status = 0;
		
		switch(status)
		{
		case 1:System.out.println("i am @College");break; 
		case 2:System.out.println("I am @Office");break;
		case 3:System.out.println("I am @Home");break;
		default:System.out.println("Not at college,office,home");break;
		}
		
	}

}
